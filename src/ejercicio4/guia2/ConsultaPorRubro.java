       private void agregarCabecera(){
        modelo.addColumn("Codigo");
        modelo.addColumn("Descripion");
        modelo.addColumn("Precio");
        modelo.addColumn("Stock");
        jTProductos.setModel(modelo);
        
    }
    private void borrarFilas(){
        int f=jTProductos.getRowCount()-1;
        for(;f>=0;f--){
            modelo.removeRow(f);
        }
    }
    private void cargarComboBox(){
        jcbrubros.addItem(Categorias.SELECCIONA_UNA_CATEGORIA);
        jcbrubros.addItem(Categorias.ELECTRONICO);
        jcbrubros.addItem(Categorias.ALIMENTICIO);
        jcbrubros.addItem(Categorias.FERRETERIA);
        jcbrubros.addItem(Categorias.INDUMENTARIA);
        jcbrubros.addItem(Categorias.PANADERIA);
        jcbrubros.addItem(Categorias.PERFUMERIA);
    }
