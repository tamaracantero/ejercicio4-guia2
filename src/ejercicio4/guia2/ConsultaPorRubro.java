    private void jcbrubrosItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbrubrosItemStateChanged
        borrarFilas();
        
        Categorias seleccion=(Categorias)jcbrubros.getSelectedItem();
        
        for (Producto prod : MenuDeTodoSA.inventario) {
            if(prod.getCategoria()==seleccion){
                modelo.addRow(new Object[]{prod.getCodigo(),prod.getDescripcion(),prod.getPrecio(),prod.getStock()});
            }
        }
    }//GEN-LAST:event_jcbrubrosItemStateChanged


